# 매 시각을 [ 문자열로 바꾼 다음 ] 문자열에 '3'이 포함됐는지 검사하는 방법
# 1초씩 늘려가며 시, 분, 초를 문자열 자료형으로 변환하여 합친다.
# 03시 20분 35초일때를 --> '032035'만들어 '3'이 포함되어있는지 체크하는 방식.
# H를 입력받기
h = int(input())

count = 0
start_time = time.time()
for i in range(h + 1):
    for j in range(60):
        for k in range(60):
            # 매 시각 안에 '3'이 포함되어 있다면 카운트 증가
            if '3' in str(i) + str(j) + str(k):
                count += 1
end_time = time.time()  # 성능 측정 종료 .
print("성능 측정 : ", end_time-start_time)  
# 모든 시각 중에 3이 하나라도 포함되어 있는 모든 경우의 수 
print("답: ",count)
